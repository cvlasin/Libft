#include "libft.h"

char	*ft_strmap(const char *s, char (*f)(char))
{
	int		i;
	int		len;
	char	*str;

	if (s && f)
	{
		len = ft_strlen((char*)s);
		i = 0;
		if (!(str = (char*)malloc((len + 1) * sizeof(char))))
			return (NULL);
		while (s[i] != '\0')
		{
			str[i] = f(s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
