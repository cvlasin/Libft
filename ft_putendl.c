#include <unistd.h>
#include "libft.h"

void	ft_putendl(const char *s)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar(s[i]);
		i++;
	}
	write(1, "\n", 1);
}
